DROP PROCEDURE IF EXISTS [dbo].[sp_CreatePatient]
GO

CREATE PROCEDURE [dbo].[sp_CreatePatient]
	@UserId int
AS
BEGIN TRY
	SET NOCOUNT ON;

	INSERT INTO [dbo].[Patients] (UserId) VALUES (@UserId)

	RETURN @@IDENTITY
END TRY
BEGIN CATCH
	RETURN -1
END CATCH
GO